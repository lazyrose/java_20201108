package 반복문;

public class 문제3 {

	public static void main(String[] args) {
		// 실습
		for (int i = 0; i < 10; i++) {
			System.out.print("*");
		}
		
		System.out.println();
		
		for (int i = 0; i < 5; i++) {
			System.out.print("커피*");
		}
		
		for (int i = 0; i < 3; i++) {
			System.out.println("커피*우유");
		}
		
		for (int i = 0; i < 3; i++) {
			System.out.println((i + 1) + " : 짱!");
		}

	}

}
